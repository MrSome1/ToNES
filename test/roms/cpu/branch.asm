
.segment "NES_FILE_HEADER"
.byte "NES", $1A, 2

.segment "CODE"
main:        ;  Addr
    BCC lbvc ; 0x8000 @REG A=0x00 X=0x00 Y=0x00 S=0xfd P=0b00100100 PC=0x8003
    NOP      ; 0x8002

lbvc:
    BVC lbpl ; 0x8003 @REG A=0x00 X=0x00 Y=0x00 S=0xfd P=0b00100100 PC=0x8006
    NOP      ; 0x8005

lbpl:
    BPL lbne ; 0x8006 @REG A=0x00 X=0x00 Y=0x00 S=0xfd P=0b00100100 PC=0x8009
    NOP      ; 0x8008

lbne:
    BNE lbcs ; 0x8009 @REG A=0x00 X=0x00 Y=0x00 S=0xfd P=0b00100100 PC=0x800c
    NOP      ; 0x800b

lbcs:
    SEC      ; 0x800c @REG A=0x00 X=0x00 Y=0x00 S=0xfd P=0b00100101 PC=0x800d
    BCS lbit ; 0x800d @REG A=0x00 X=0x00 Y=0x00 S=0xfd P=0b00100101 PC=0x8010
    NOP      ; 0x800f

lbit:
    LDA #$c0 ; 0x8010 @REG A=0xc0 X=0x00 Y=0x00 S=0xfd P=0b10100101 PC=0x8012
    STA  $0  ; 0x8012 @REG A=0xc0 X=0x00 Y=0x00 S=0xfd P=0b10100101 PC=0x8014 @RAM 0x0000 0xc0
    LDA #$0c ; 0x8014 @REG A=0x0c X=0x00 Y=0x00 S=0xfd P=0b00100101 PC=0x8016
    BIT  $0  ; 0x8016 @REG A=0x0c X=0x00 Y=0x00 S=0xfd P=0b11100111 PC=0x8018

    BVS lbmi ; 0x8018 @REG A=0x0c X=0x00 Y=0x00 S=0xfd P=0b11100111 PC=0x801b
    NOP      ; 0x801a

lbmi:
    BMI lbeq ; 0x801b @REG A=0x0c X=0x00 Y=0x00 S=0xfd P=0b11100111 PC=0x801e
    NOP      ; 0x801d

lbeq:
    BEQ main ; 0x801e @REG A=0x0c X=0x00 Y=0x00 S=0xfd P=0b11100111 PC=0x8000

.segment "INTERRUPT_VECTOR"
    .word main, main, main