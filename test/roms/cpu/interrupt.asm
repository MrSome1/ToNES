
.segment "NES_FILE_HEADER"
.byte "NES", $1A, 2

.segment "CODE"
main:
    BRK ; 0x8000 @REG A=0x00 X=0x00 Y=0x00 S=0xfa P=0b00110100 PC=0x8004
    NOP ; 0x8001

nmi:
    NOP ; 0x8002
    RTI ; 0x8003

irq:
    CLI ; 0x8004 @REG A=0x00 X=0x00 Y=0x00 S=0xfa P=0b00110000 PC=0x8005
    NOP ; 0x8005 @REG A=0x00 X=0x00 Y=0x00 S=0xfa P=0b00110000 PC=0x8006
    RTI ; 0x8006 @REG A=0x00 X=0x00 Y=0x00 S=0xfd P=0b00110100 PC=0x8002

.segment "INTERRUPT_VECTOR"
    .word nmi, main, irq